function cloneInitialDiaryEntries() {
  return JSON.parse(JSON.stringify(INITIAL_DIARY_ENTRIES))
}

const DiaryEntries = {
  key: 'diaryEntries',
  entries: cloneInitialDiaryEntries(),

  /**
   * sessionStorage から日記を読み込む
   */
  load() {
    const stored = sessionStorage.getItem(this.key)
    if (!stored) {
      this.entries = cloneInitialDiaryEntries()
      return
    }

    try {
      const parsed = JSON.parse(stored)
      if (Array.isArray(parsed)) {
        this.entries = parsed
      } else {
        this.entries = cloneInitialDiaryEntries()
      }
    } catch (error) {
      console.error('Failed to load diary entries:', error)
      this.entries = cloneInitialDiaryEntries()
    }
  },

  /**
   * sessionStorage に日記を保存
   */
  save() {
    sessionStorage.setItem(this.key, JSON.stringify(this.entries))
  },

  /**
   * 初期状態に戻す
   */
  resetToInitial() {
    this.entries = cloneInitialDiaryEntries()
    sessionStorage.removeItem(this.key)
  },

  /**
   * 全エントリを取得（新着順）
   * @param {boolean} onlyPublic - true の場合、公開のみを取得
   */
  getAll(onlyPublic = false) {
    let entries = [...this.entries]
    if (onlyPublic) {
      entries = entries.filter(e => e.isPublic)
    }
    return entries.sort((a, b) => new Date(b.date) - new Date(a.date))
  },

  /**
   * 特定の日付のエントリを取得
   */
  getByDate(date) {
    return this.entries.find(e => e.date === date)
  },

  /**
   * アーカイブを月別で取得
   * @param {boolean} onlyPublic - true の場合、公開のみを取得
   */
  getArchive(onlyPublic = false) {
    let entries = this.entries
    if (onlyPublic) {
      entries = entries.filter(e => e.isPublic)
    }
    
    const archive = {}
    entries.forEach(entry => {
      const [year, month] = entry.date.split('-')
      const key = `${year}-${month}`
      if (!archive[key]) {
        archive[key] = []
      }
      archive[key].push(entry)
    })

    // 新着月順でソート
    const sorted = {}
    Object.keys(archive)
      .sort()
      .reverse()
      .forEach(key => {
        sorted[key] = archive[key].sort((a, b) => new Date(b.date) - new Date(a.date))
      })

    return sorted
  },

  /**
   * 新規エントリを追加
   */
  addEntry(title, content) {
    const now = new Date()
    const date = now.toISOString().split('T')[0]
    this.entries.push({ date, title, content, isPublic: true })
    this.save()
  },

  /**
   * あずさ謝罪日記（公開）を1件追加
   */
  addAzusaApologyEntry() {
    const title = 'あずさより'
    const content = '　このサイトを勝手に触ってしまって、ごめんなさい。\n\n\n　不安にさせるようなことをしてしまいました。\n\n\n　ぐりむさんにも、ここを見ていたみなさんにも、迷惑をかけました。\n\n\n　本当に申し訳ありません。'
    const now = new Date()
    const date = now.toISOString().split('T')[0]
    this.entries.push({
      date,
      title,
      content,
      isPublic: true,
    })
    this.save()
  },
}
